#include <REGX52.H>
#include "Matrix_LED.h"

unsigned char Animation[] = {
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0xFF,0x08,0x08,0x08,0xFF,0x00,0x0E,0x15,
	0x15,0x15,0x08,0x00,0x7E,0x01,0x02,0x00,
	0x7E,0x01,0x02,0x00,0x0E,0x11,0x11,0x0E,
	0x00,0x7D,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
};

void main(void)
{
	unsigned char i, offset = 0, cnt = 0;
	MatrixLED_Init();
	//P0 = 0;
	//_74HC595_WriteByte(0xaa);
	//MatricLED_ShowColumn(3, 0x40);
	while(1)
	{
		for(i = 0; i < 8; i++)
		{
			MatricLED_ShowColumn(i, Animation[i+offset]);
		}
		cnt++;
		if(cnt > 10)
		{
			cnt = 0;
			offset++;
			if(offset > 32)
			{
				offset = 0;
			}
		}
	}
}